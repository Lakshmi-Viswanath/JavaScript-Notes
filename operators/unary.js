// unary operator  - ++ , -- , - , + , typeof
let a = 10;
let y = a++;
console.log("value of a", a); //11
console.log("value of y", y); //10

let x = 10;
let b = ++x;
console.log("value of x", x); //11
console.log("value of b", b); //11

console.log(10 + "hello"); //10hello
console.log(15 - "10"); //5

